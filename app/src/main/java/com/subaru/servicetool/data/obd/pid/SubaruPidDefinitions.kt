/**
 * Canonical definitions for all known Subaru vehicle PIDs.
 *
 * Every entry specifies the complete metadata for one sensor: protocol, address,
 * length, scaling formula, unit, priority, and decoder — making the definition
 * self-documenting and independent of any polling infrastructure.
 *
 * ## Adding a new PID
 * Add a new [PidDefinition] to [ALL] (or call [SubaruPidRegistry.register] at runtime).
 * No changes to pollers, parsers, transports, or the registry itself are required.
 *
 * ## Protocols in use
 * - [PidProtocol.OBD2]  — SAE J1979 Mode 01, any ELM327 adapter
 * - [PidProtocol.SSM2]  — Subaru A8 service, 3-byte addresses, CAN ISO-TP
 * - [PidProtocol.SSM4]  — Subaru A8 service, 4-byte addresses (future ECUs)
 * - [PidProtocol.UDS22] — ISO 14229 Mode 22, used for ECU Mode 21/22 and BCM/TPMS
 *
 * Aliases for commonly-used decoder combos:
 */
object SubaruPidDefinitions {

